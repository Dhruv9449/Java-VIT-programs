public class LiteralIntegers {
    public static void main(String[] arg) {
        int decimalInt = 1234;
        int octalInt = 077;
        int hexaInt = 0x1ff2;
        int binInt = 0b1010101;
        System.out.println("This is a Decimal Literal: " + decimalInt);
        System.out.println("This is an Octal Literal: " + octalInt);
        System.out.println("This is a Hexa Decimal Literal: " + hexaInt);
        System.out.println("This is a Binary Literal: " + binInt);
    }
}

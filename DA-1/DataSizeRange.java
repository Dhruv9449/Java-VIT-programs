class DataSizeRange {
    public static void main(String[] args) {
        // Byte
        System.out.println("Long Min: " + Long.MIN_VALUE +
                "\nMax: " + Long.MAX_VALUE +
                "\nBytes: " + Long.BYTES);
        System.out.println();

        // Short
        System.out.println("Short \nMin: " + Short.MIN_VALUE +
                "\nMax: " + Short.MAX_VALUE +
                "\nBytes: " + Short.BYTES);
        System.out.println();

        // Integer
        System.out.println("Int \nMin: " + Integer.MIN_VALUE +
                "\nMax: " + Integer.MAX_VALUE +
                "\nBytes: " + Integer.BYTES);
        System.out.println();

        // Long
        System.out.println("Long \nMin: " + Long.MIN_VALUE +
                "\nMax: " + Long.MAX_VALUE +
                "\nBytes: " + Long.BYTES);
        System.out.println();

        // Float
        System.out.println("Float \nMin: " + Float.MIN_VALUE +
                "\nMax: " + Float.MAX_VALUE +
                "\nBytes: " + Float.BYTES);
        System.out.println();

        // Double
        System.out.println("Double \nMin: " + Double.MIN_VALUE +
                "\nMax: " + Double.MAX_VALUE +
                "\nBytes: " + Double.BYTES);
        System.out.println();

        // Character
        System.out.println("Character \nMin: " + Character.MIN_VALUE +
                "\nMax: " + Character.MAX_VALUE +
                "\nBytes: " + Character.BYTES);
        System.out.println();

        // Boolean
        System.out.println("Boolean \nMin: " + Boolean.TRUE +
                "\nMax: " + Boolean.FALSE);
    }
}
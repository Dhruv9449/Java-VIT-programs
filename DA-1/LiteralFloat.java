public class LiteralFloat {
    public static void main(String[] arg) {
        float valFloat = 1.7732f;
        double doubleFloat = 1.7732d;
        float expFloat = 123E4f;
        System.out.println("This is a Floating Point Literal: " + valFloat);
        System.out.println("This is a Decimal Literal: " + doubleFloat);
        System.out.println("This is an Exponential Literal: " + expFloat);
    }
}
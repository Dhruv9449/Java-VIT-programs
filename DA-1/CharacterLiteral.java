public class CharacterLiteral {
    public static void main(String[] args) {
        char character = 'd';
        char unicodeCharacter = '\u0064';
        System.out.println("This is Character literal: " + character);
        System.out.println("This is unicode character literal: " + unicodeCharacter);
    }
}
